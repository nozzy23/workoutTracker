const router = require("express").Router();
const Workout = require("../model/workout.js");

router.put("/exercise", ({ body }, res) => {
  Workout.create(body)
    .then(dbWorkout => {
      res.json(dbWorkout);
    })
    .catch(err => {
      res.status(400).json(err);
    });
});


module.exports = router;